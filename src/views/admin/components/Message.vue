<template>
    <div class="message-container">
        <div :class="getPosition">
            {{ message.text }}
        </div>
    </div>
</template>

<script>
    export default {
        name: 'message',
        props: {
            message: {
                type: Object,
                required: true
            }
        },
        computed: {
            getPosition () {
                if (this.message.sender === this.$current_user.uid) {
                    return 'my message'
                } else {
                    return 'message'
                }
            }
        }
    }
</script>

<style scoped>
    .my.message {
        text-align: right;
    }
    .message-container {
        border: 1px solid silver;
    }
</style>